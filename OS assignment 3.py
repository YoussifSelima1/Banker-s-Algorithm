class MainWindow(QtWidgets.QMainWindow, Ui_MainWindow):
    def __init__(self, parent=None):
        super(MainWindow, self).__init__(parent)
        self.setupUi(self)
        
        self.processbutton.clicked.connect(self.add_process)
        self.resourcebutton.clicked.connect(self.add_resource)
        self.calculatebutton.clicked.connect(self.calculate)

        self.processes = []
        self.resources = []
        self.allocation = []
        self.max_allocation = []

    def add_process(self):
        process = []
        process.append(self.lineEdit.text())
        process.append(self.lineEdit_2.text())
        process.append(self.lineEdit_3.text())
        process.append(self.lineEdit_5.text())
        process.append(self.lineEdit_5.text())
        self.processes.append(process)
        self.update_table(self.allocationtable, self.processes)

    def add_resource(self):
        resource = [self.lineEdit_4.text(), self.lineEdit_6.text(), self.lineEdit_7.text(), self.lineEdit_8.text()]
        self.resources.append(resource)
        self.update_table(self.maxallocation, self.resources)

    def calculate(self):
        self.get_data()

        # Calculate the need matrix
        need_matrix = []
        for i in range(len(self.processes)):
            allocation_row = self.allocation[i][1:]
            max_allocation_row = self.max_allocation[i][1:]
            need_row = list(map(lambda x, y: int(y) - int(x), allocation_row, max_allocation_row))
            need_matrix.append([self.allocation[i][0]] + need_row)

        # Calculate the available resources vector
        available_resources = self.calculate_available_resources()

        # Check if the system is in a safe state
        safe_sequence = self.check_safe_state(need_matrix, available_resources)

        # Show the result
        if safe_sequence:
            QtWidgets.QMessageBox.information(self, 'Safe state', f'Found a safe sequence: {", ".join(safe_sequence)}')
        else:
            QtWidgets.QMessageBox.warning(self, 'Unsafe state', 'The system is in an unsafe state')

    def get_data(self):
        self.allocation = self.get_table_data(self.allocationtable)
        self.max_allocation = self.get_table_data(self.maxallocation)
        self.resources = self.get_table_data(self.availablematrix)

    def get_table_data(self, table):
        data = []
        for row in range(table.rowCount()):
            row_data = []
            for column in range(table.columnCount()):
                item = table.item(row, column)
                row_data.append(item.text())
            data.append(row_data)
        return data

    def update_table(self, table, data):
        table.setRowCount(len(data))
        for i, row_data in enumerate(data):
            for j, cell_data in enumerate(row_data):
                item = QtWidgets.QTableWidgetItem(cell_data)
                table.setItem(i, j, item)

    def calculate_available_resources(self):
        available_resources = []
        for i in range(len(self.resources[0])):
            total = sum(int(resource[i]) for resource in self.resources)
            allocated = sum(int(process[i + 1]) for process in self.allocation)
            available = total - allocated
            available_resources.append(str(available))
        return available_resources

    def check_safe_state(self, need_matrix, available_resources):
        processes_done = [False] * len(self.processes)
        safe_sequence = []

        while not all(processes_done):
            found = False
            for i, process in enumerate(self.processes):
                if not processes_done[i] and all(int(need) <= int(available_resources[j]) for j, need in enumerate(need_matrix[i][1:])):
                    processes_done[i] = True
                    available_resources = list(map(lambda x,y: str(int(x)+int(y)), available_resources, self.allocation[i][1:]))
                    safe_sequence.append(process[0])
                    found = True
            if not found:
                break

        return safe_sequence if len(safe_sequence) == len(self.processes) else None

